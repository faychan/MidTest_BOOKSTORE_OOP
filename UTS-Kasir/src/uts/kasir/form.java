    private void selectData() {
        String C[] = {"id", "ItemName", "ItemAmount", "ItemPrice","TotalPrice"};
        DefaultTableModel dtm = new DefaultTableModel(null, C);
        String SQL = "SELECT * FROM kasir";
        ResultSet rs= connectDB.executeQuery(SQL);
        
        try {
            while(rs.next()) {
                String id = rs.getString(1);
                String ItemName = rs.getString(2);
                String ItemAmount = rs.getString(3);
                String ItemPrice = rs.getString(4);
                String TotalPrice = rs.getString(5);
                String data[] = {id, ItemName, ItemAmount, ItemPrice,TotalPrice};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(form.class.getName()).log(Level.SEVERE, null, ex);
        } tb1.setModel(dtm);
    }
